# Using python as a calculator

# # Numbers
# # # Addition
add = 6 + 5
print(add)

# # # Division
div = 6 / 5
print(div)  # Always returns a float
div = 6 // 5  # Floor-division, discards decimals
print(div)
mod = 6 % 5
print(mod)  # Modulus, %, returns remainder

# Powers
print(6 ** 5)  # Prints 6^5
print(5 ** 6)  # Prints 5^6





